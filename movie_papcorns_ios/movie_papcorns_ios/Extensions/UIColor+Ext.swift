import UIKit

extension UIColor {
    
    static func black75() -> UIColor {
        return UIColor.black.withAlphaComponent(0.75)
    }
    
    static func labelColor2() -> UIColor {
        return UIColor(red: 153/255, green: 153/255, blue: 153/255, alpha: 1)
    }
    
    static func background2() -> UIColor {
        return UIColor(red: 22/255, green: 22/255, blue: 22/255, alpha: 1)
    }
    
    static func background3() -> UIColor {
        return UIColor(red: 36/255, green: 36/255, blue: 36/255, alpha: 1)
    }
}
